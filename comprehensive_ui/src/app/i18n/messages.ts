export type MessageKey = string;

const getByKey = (source: Record<string, unknown>, key: string): string | null => {
  const parts = key.split(".");
  let current: unknown = source;
  for (const part of parts) {
    if (!current || typeof current !== "object" || !(part in current)) {
      return null;
    }
    current = (current as Record<string, unknown>)[part];
  }
  return typeof current === "string" ? current : null;
};

export const formatMessage = (template: string, params?: Record<string, string | number>): string => {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    if (!(key in params)) return `{${key}}`;
    return String(params[key]);
  });
};

export const resolveMessage = (language: Language, key: MessageKey): string => {
  const localized = getByKey(messages[language] as unknown as Record<string, unknown>, key);
  if (localized) return localized;
  const english = getByKey(messages.en as unknown as Record<string, unknown>, key);
  return english ?? key;
};
